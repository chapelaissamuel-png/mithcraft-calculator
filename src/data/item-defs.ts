function buildItem(
  [id, short, color, tier = 'common', type = 'item']: RawDef,
  mod: string,
): ItemMeta {
  const key = id.split(':')[1] || id;
  const name = MOD_NAMES[id] || key.split('_').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join(' ');
  return { id, name, mod, tier, type, short, color, tags: [], maxStack: 64 };
}

