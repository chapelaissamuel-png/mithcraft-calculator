export function buildItemRegistry(): Map<string, ItemMeta> {
  const map = new Map<string, ItemMeta>();
  for (const { mod, defs } of ALL_DEFS) {
    for (const d of defs) {
      const item = buildItem(d, mod);
      map.set(item.id, item);
    }
  }
  return map;
}
